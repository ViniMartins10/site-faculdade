// Função para obter o valor de um parâmetro da URL
function getURLParameter(name) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(name);
}

// Quando a página carregar
window.onload = function() {
    // Obtém o id do professor da URL (por exemplo, ?id=adison)
    const professorId = getURLParameter('id');

    // Verifica se o id existe e se há dados para esse professor
    if (professorId && professores[professorId]) {
        const professor = professores[professorId];

        // Preenche os elementos HTML com as informações do professor
        document.getElementById('foto-professor').src = professor.foto;
        document.getElementById('foto-professor').alt = `Foto de ${professor.nome}`;
        document.getElementById('nome-professor').textContent = professor.nome;
        document.getElementById('biografia').textContent = professor.biografia;
        document.getElementById('temas').textContent = professor.temas;
        document.getElementById('contato').textContent = professor.contato;
        document.getElementById('linkedin').href = professor.linkedin;
        document.getElementById('lattes').href = professor.lattes;
    } else {
        // Caso o id não seja válido ou não exista, exibe uma mensagem de erro
        document.body.innerHTML = '<h1>Professor não encontrado!</h1><p>O professor solicitado não existe. Verifique o ID e tente novamente.</p>';
    }
};
